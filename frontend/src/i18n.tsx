export function normalizeUiLocale(value?: string | null): UiLocale {
  const language = String(value || "").trim().toLowerCase();
  if (language.startsWith("zh")) return "zh-CN";
  if (language.startsWith("en")) return "en-US";
  return DEFAULT_LOCALE;
}

export function detectBrowserLocale(): UiLocale {
  if (typeof window === "undefined") return DEFAULT_LOCALE;
  const stored = window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY);
  if (stored) return normalizeUiLocale(stored);
  const browserLanguage = navigator.languages?.[0] || navigator.language;
  return normalizeUiLocale(browserLanguage);
}

export function getCurrentUiLocale(): UiLocale {
  if (typeof document !== "undefined") {
    const htmlLang = document.documentElement.lang;
    if (htmlLang) return normalizeUiLocale(htmlLang);
  }
  if (typeof window !== "undefined") {
    const stored = window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY);
    if (stored) return normalizeUiLocale(stored);
  }
  return DEFAULT_LOCALE;
}

export function translate(locale: UiLocale, key: string): string {
  return translations[locale][key] || translations[DEFAULT_LOCALE][key] || key;
}

type I18nContextValue = {
  locale: UiLocale;
  setLocale: (locale: UiLocale) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue>({
  locale: DEFAULT_LOCALE,
  setLocale: () => undefined,
  t: (key) => translate(DEFAULT_LOCALE, key),
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<UiLocale>(() => detectBrowserLocale());

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, locale);
    }
    if (typeof document !== "undefined") {
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key: string) => translate(locale, key),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
